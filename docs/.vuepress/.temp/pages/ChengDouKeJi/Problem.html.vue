<h3 id="_1-3-2关于机器人售后问题" tabindex="-1"><a class="header-anchor" href="#_1-3-2关于机器人售后问题" aria-hidden="true">#</a> 1.3.2关于机器人售后问题</h3>
<p><strong>反馈模板</strong>：平台名称，商品名称，订单号，反馈的问题</p>
<ul>
<li><strong>处理方法</strong>：
<blockquote>
<p>1.客服在售后群里发起售后订单后，去询问商家是否给退。<br>
2.询问商家时要描述清楚，比如该商品确认收货已经超过七天，若商家同意退先通知客服可以退，然后登记到共享表格里，接着需要和财务核对推荐人直推佣金有没有发放，发放了要扣除，然后再扣除购买者代言人、合伙人奖励、以及身份，以上操作完成之后等仓库收到货，就可以处理售后了。</p>
</blockquote>
</li>
</ul>
<p><strong>注意事项</strong>：<span class="red_text">在这期间一定要对售后订单跟进到位，确保问题得到了完整解决。跨周退款的机器人要扣除代言人奖励和合伙人奖励奖金池</span></p>
<h3 id="_1-3-3关于商品维权订单问题" tabindex="-1"><a class="header-anchor" href="#_1-3-3关于商品维权订单问题" aria-hidden="true">#</a> 1.3.3关于商品维权订单问题</h3>
<p><strong>反馈模板</strong>：平台名称，商品名称，订单号，反馈的问题</p>
